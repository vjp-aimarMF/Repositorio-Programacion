public class Apartado14 {

    final static double PI = 3.14;
    public static void main(String[] args) {
        // Radio de la circunferencia
        double radio = 5.2;

        // Calcular el área (fórmula: A = PI * r^2)
        double area = PI * radio * radio;

        // Mostrar el resultado
        System.out.println("El área de una circunferencia cuyo radio vale " + radio + " cm sería igual a: " + area + " cm²");
    }
}
